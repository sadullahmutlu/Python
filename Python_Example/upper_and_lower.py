#upper() girilen string ifadeyi komple büyük hale getirir
#lower() girilen string ifadeyi komple küçük hale getirir
v = "Burada yeni şeyler öğreniyorum"

print(v.upper())
print(v.lower())

#her zaman gelen metni göremeye bilirz bu tür durumlarda islower ve isupper 
#methodları kullanırız böylece büyük mü küçük mü olduğunu anlayabiliriz

print(v.isupper())
print(v.islower())

