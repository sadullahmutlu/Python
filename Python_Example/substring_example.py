#elimizdeki stringlerin alt stringlerine/elemanlarına ulaşmamızı sağlar


a = "one two three four five"

print(a[0])#0 indexindeki elemanı verir
print(a[1])#1 indexindeki elemanı verir
print(a[2])#2 indexindeki elemanı verir
print(a[0:3])#0 indexsinden 3'e kadar olan karakterleri verir
print(a[4:7])#4 indexsinden 7'ye kadar olan karakterleri verir
print(a[8:13])#8 indexsinden 13'e kadar olan karakterleri verir
print(a[14:18])#14 indexsinden 18'e kadar olan karakterleri verir
print(a[19:23])#19 indexsinden 23'e kadar olan karakterleri verir
